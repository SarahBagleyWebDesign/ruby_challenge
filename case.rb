current_weather = 'sunny'
case current_weather
when 'sunny'
	puts "Shave your legs & get some shorts on, girl!"
when 'cloudy'
	puts "Run to Starbucks and get a PSL, yo!"
when 'foggy'
	puts "Stay inside and look out the window at the gorgeous fog."
when 'rainy'
	puts "Get those wellies on, baby!"
when 'hurricane'
	puts "Better stay here in Oregon where we don't have those."
else
	puts "I have no idea what the H is going on."
end
